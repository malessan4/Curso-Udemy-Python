operandoA = 3
operandoB = 2
suma = operandoA + operandoB
# print('El resultado de la suma:', suma)

print(f'El resultado de la suma: {suma}')

resta = operandoA - operandoB
print(f'El resultado de la resta: {resta}')

multiplicacion = operandoA * operandoB
print(f'El resultado de la multiplicacion: {multiplicacion}')

# division pero en tipo flotante
division = operandoA / operandoB
print(f'El resultado de la division: {division}')

# se divide pero en tipo entero
division = operandoA // operandoB
print(f'El resultado de la division: {division}')

modulo = operandoA % operandoB
print(f'El resultado del residuo division (modulo): {modulo}')

# la base es el operandoA y la elevacion es operandoB
exponente = operandoA ** operandoB
print(f'El resultado del exponente: {exponente}')