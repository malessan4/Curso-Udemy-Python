a = 4
b = 2

# es igual a...
resultado = a == b
print(f'resultado == : {resultado}')

# es diferente a...
resultado = a != b
print(f'resultado != : {resultado}')

resultado = a > b
print(f'resultado > : {resultado}')

resultado = a >= b
print(f'resultado >= : {resultado}')

resultado = a < b
print(f'resultado < : {resultado}')